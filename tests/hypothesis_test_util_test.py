import functools
import os

from absl.testing import absltest
import hypothesis as hp
from jax._src import hypothesis_test_util as htu
from jax._src import test_util as jtu

shard_index = int(os.environ.get("TEST_SHARD_INDEX", "0"))
total_shards = int(os.environ.get("TEST_TOTAL_SHARDS", "1"))


@jtu.skip_under_pytest("The test sharding runner is bazel-specific")
class HypothesisTest(htu.HypothesisShardedTestCase):
  """Tests that Hypothesis tests and standard tests are sharded correctly."""

  _hyp_calls = []
  _all_generated_x = []

  @classmethod
  def setUpClass(cls):
    super().setUpClass()
    # Intercept inner_test to track all candidates generated by hypothesis
    hyp_handle = cls.test_hyp.hypothesis
    current_inner = hyp_handle.inner_test

    # We must use the exact original inner test when testing our shard hashes.
    # current_inner is the wrapper applied by HypothesisShardedTestCase, so
    # its __wrapped__ is the raw inner test function (if sharded).
    cls._original_inner_test = getattr(current_inner, "__wrapped__", current_inner)

    @functools.wraps(current_inner)
    def tracking_wrapper(*args, **kwargs):
      x_val = kwargs.get("x")
      if x_val is None and len(args) > 1:
        x_val = args[1]
      cls._all_generated_x.append(x_val)
      return current_inner(*args, **kwargs)

    hyp_handle.inner_test = tracking_wrapper

  @hp.given(x=hp.strategies.integers(min_value=0, max_value=10000))
  @hp.settings(
      max_examples=100,
      suppress_health_check=[hp.HealthCheck.too_slow],
      database=None,
  )
  def test_hyp(self, x):
    # This hypothesis test is subject to inner-sharding and will run iterations
    # distributed across shards. We track the examples that this shard actually
    # executes.
    type(self)._hyp_calls.append(x)

  def test_m_std_a(self):
    # We expect non-hypothesis tests to be assigned to shards round-robin.
    self.assertEqual(shard_index, 0)

  def test_m_std_b(self):
    self.assertEqual(shard_index, 1 % total_shards)

  def test_m_std_c(self):
    self.assertEqual(shard_index, 2 % total_shards)

  @classmethod
  def tearDownClass(cls):
    # Check that the current shard evaluated exactly the subset of candidates
    # it should have.
    test_instance = cls("test_hyp")

    expected_calls = set()
    for x in cls._all_generated_x:
      shard = htu.hypothesis_inner_test_shard(
          cls._original_inner_test, (test_instance,), {"x": x}, total_shards
      )
      if shard == shard_index:
        expected_calls.add(x)

    actual_calls = set(cls._hyp_calls)
    assert actual_calls == expected_calls, (
        "Mismatch between expected executions and actual executions on shard"
        f" {shard_index}!\nMissing: {expected_calls - actual_calls}\nExtra:"
        f" {actual_calls - expected_calls}"
    )

    super().tearDownClass()


@jtu.skip_under_pytest("The test sharding runner doesn't run under pytest")
class SingleShardTest(jtu.JaxTestCase):
  # Standard test cases should be assigned to shard round-robin.

  def test_z_absl_std1(self):
    self.assertEqual(shard_index, 0)

  def test_z_absl_std2(self):
    self.assertEqual(shard_index, 1 % total_shards)

  def test_z_absl_std3(self):
    self.assertEqual(shard_index, 2 % total_shards)


if __name__ == "__main__":
  absltest.main(testLoader=htu.HypothesisShardedTestLoader())
